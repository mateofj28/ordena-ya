class Product {
  String id;
  String name;
  String description;
  String preparationTime;
  double unitPrice;
  String imageUrl;
  int quantity;
  String category;
  String notes;

  Product({
    required this.id,
    required this.name,
    required this.description,
    required this.preparationTime,
    required this.unitPrice,
    required this.imageUrl,
    required this.quantity,
    required this.category,
    required this.notes
  });
}
